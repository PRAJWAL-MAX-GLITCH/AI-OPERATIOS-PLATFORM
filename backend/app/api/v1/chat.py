@router.get(
    "/chat/sessions",
    response_model=ApiResponse[List[SessionSummary]],
    summary="List conversation sessions",
)
async def list_sessions(
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
) -> ApiResponse[List[SessionSummary]]:
    """List all active sessions for the authenticated user."""
    sessions = await chat_service.list_sessions(db, current_user.id)
    return ApiResponse(data=[SessionSummary.model_validate(s) for s in sessions])


@router.get(
    "/chat/sessions/{session_id}",
    response_model=ApiResponse[SessionDetailResponse],
    summary="Get session with full message history",
)
async def get_session(
    session_id: uuid.UUID,
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
) -> ApiResponse[SessionDetailResponse]:
    """Get a conversation session with its complete message history."""
    session = await chat_service.get_session(db, session_id, current_user.id)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")

    # Load messages
    result = await db.execute(
        select(ChatMessage)
        .where(ChatMessage.session_id == session_id)
        .order_by(ChatMessage.created_at.asc())
    )
    messages = list(result.scalars().all())

    response = SessionDetailResponse(
        id=session.id,
        title=session.title,
        knowledge_base_id=session.knowledge_base_id,
        llm_provider=session.llm_provider,
        llm_model=session.llm_model,
        total_messages=session.total_messages,
        total_tokens_used=session.total_tokens_used,
        messages=[MessageResponse.model_validate(m) for m in messages],
        created_at=session.created_at,
        updated_at=session.updated_at,
    )
    return ApiResponse(data=response)


@router.delete(
    "/chat/sessions/{session_id}",
    status_code=status.HTTP_204_NO_CONTENT,
    summary="Delete a conversation session",
)
async def delete_session(
    session_id: uuid.UUID,
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
) -> None:
    """Soft-delete a conversation session and all its messages."""
    deleted = await chat_service.delete_session(db, session_id, current_user.id)
    if not deleted:
        raise HTTPException(status_code=404, detail="Session not found")


@router.get(
    "/chat/sessions/{session_id}/citations",
    response_model=ApiResponse[List[SessionCitationResponse]],
    summary="Get all citations for a session",
)
async def get_session_citations(
    session_id: uuid.UUID,
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
) -> ApiResponse[List[SessionCitationResponse]]:
    """Retrieve all source citations referenced in a conversation session."""
    citations = await chat_service.get_session_citations(db, session_id, current_user.id)
    return ApiResponse(data=[SessionCitationResponse.model_validate(c) for c in citations])
