"""
Chat API Router
===============
All conversational RAG endpoints.

Endpoints:
  POST   /chat                       — Non-streaming Q&A
  POST   /chat/stream                — SSE streaming Q&A
  GET    /chat/sessions              — List user sessions
  GET    /chat/sessions/{id}         — Session detail + message history
  DELETE /chat/sessions/{id}         — Delete session
  GET    /chat/sessions/{id}/citations — All citations for session
"""
from fastapi import APIRouter, Depends, HTTPException, status
from fastapi.responses import StreamingResponse
from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy import select
from typing import List
import uuid
import structlog

from app.schemas.responses import ApiResponse
from app.schemas.chat import (
    ChatRequest, ChatResponse, CitationResponse,
    SessionSummary, SessionDetailResponse, SessionCitationResponse,
    MessageResponse,
)
from app.services.chat_service import chat_service
from app.models.domain import User
from app.models.chat import ConversationSession, ChatMessage
from app.api.dependencies import get_db, get_current_user

router = APIRouter()
logger = structlog.get_logger(__name__)


# ---------------------------------------------------------------------------
# Chat Endpoints
# ---------------------------------------------------------------------------

@router.post(
    "/chat",
    response_model=ApiResponse[ChatResponse],
    status_code=status.HTTP_200_OK,
    summary="Ask a question to the AI Knowledge Assistant",
    description=(
        "Non-streaming RAG Q&A. Retrieves relevant context from the knowledge base, "
        "sends to LLM, and returns a grounded answer with source citations."
    ),
)
async def chat(
    request: ChatRequest,
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
) -> ApiResponse[ChatResponse]:
    """Non-streaming conversational RAG endpoint."""
    try:
        result = await chat_service.chat(
            db=db,
            user_id=current_user.id,
            knowledge_base_id=request.knowledge_base_id,
            question=request.question,
            session_id=request.session_id,
        )
        return ApiResponse(data=ChatResponse(
            session_id=result.get("session_id"),
            answer=result["answer"],
            citations=[CitationResponse(**c) for c in result.get("citations", [])],
            model=result.get("model"),
            provider=result.get("provider"),
            latency_ms=result.get("latency_ms", 0),
            total_tokens=result.get("total_tokens", 0),
            safety_flagged=result.get("safety_flagged", False),
        ))
    except Exception as exc:
        logger.error("chat_endpoint_error", error=str(exc))
        raise HTTPException(status_code=500, detail=str(exc))


@router.post(
    "/chat/stream",
    summary="Stream a response from the AI Knowledge Assistant",
    description=(
        "SSE streaming RAG Q&A. Streams tokens as they are generated by the LLM. "
        "Final event includes citations and session metadata."
    ),
    response_class=StreamingResponse,
)
async def chat_stream(
    request: ChatRequest,
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
) -> StreamingResponse:
    """
    SSE Streaming Q&A endpoint.
    Events format:
      data: {"token": "...", "done": false}
      data: {"done": true, "session_id": "...", "citations": [...]}
      data: [DONE]
    """
    async def generate():
        async for chunk in chat_service.chat_stream(
            db=db,
            user_id=current_user.id,
            knowledge_base_id=request.knowledge_base_id,
            question=request.question,
            session_id=request.session_id,
        ):
            yield chunk

    return StreamingResponse(
        generate(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "X-Accel-Buffering": "no",   # Disable nginx buffering
            "Connection": "keep-alive",
        },
    )


